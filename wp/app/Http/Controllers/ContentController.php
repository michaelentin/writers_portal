     */
    public function store(Request $req)
    {
        // Use request('form_portion_name_here') to obtain different sections of the form
        $c = new \App\Content();
      	$c->user_id = Auth::user()->id;
      	$c->title = $req->title;
        $c->summary = $req->summary;
      	$c->save();
        return redirect('/home');
    }

